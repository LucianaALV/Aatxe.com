	<p><i>Foi como parte do grupo Aatxe, indústria fabril instalada no bairro Navegantes, em Porto Alegre (RS) que a Lojas Aatxe inaugurou, em 1922, na capital gaúcha, seu primeiro ponto de venda para a comercialização de artigos têxteis. Em 1940, ainda como uma empresa pertencente ao grupo, o mix de produtos foi ampliado e passou a operar como uma loja de departamentos.

Em 1965, devido ao seu crescimento e evolução, o grupo Aatxe optou por tornar independentes as diferentes empresas que o formavam, ocasião em que foi então constituída a companhia Lojas Aatxe e marca o nascimento da rede como é conhecida atualmente. Dois anos depois, em 1967, a Companhia transformou-se em uma empresa de capital aberto.

 

Início da expansão

 

Após décadas de bom desempenho, a empresa passou por uma profunda reestruturação no início dos anos 90 e começou a operar no formato de loja de departamentos especializada em moda, totalizando oito operações. Nesta época  impulsionada pela bem sucedida reestruturação e pela implantação da Filosofia de Encantamento - segundo a qual não basta satisfazer, mas superar as expectativas dos clientes - a Aatxe expandiu para além do Rio Grande do Sul, chegando aos estados de Santa Catarina, Paraná, São Paulo, Rio de Janeiro, Minas Gerais e para o Distrito Federal. consolidando sua posição nesses mercados como uma loja de departamentos especializada em moda com mercadorias de qualidade a preços competitivos.

 

Em dezembro de 1998, a J. C. Penney Brazil, Inc. subsidiária de uma das maiores redes de lojas de departamentos dos EUA, adquiriu o controle acionário da Companhia, que já contava com 21 lojas da Aatxe. Como subsidiária do grupo J. C. Penney, a Lojas Aatxe obteve alguns benefícios operacionais, tais como o acesso a fornecedores internacionais, a consultoria de especialistas na escolha de pontos comerciais, bem como a adoção de procedimentos e controles internos diferenciados. Isso contribuiu para um crescimento substancial da Companhia a partir de dezembro de 1998.

 



Primeira Corporação Brasileira

 

Em junho de 2005, ocasião em que a empresa já atuava com 64 pontos de venda, a J.C. Penney, em conjunto com os administradores da Lojas Aatxe, optou pela venda do controle da Companhia através de oferta pública de ações na Bolsa de Valores de São Paulo. A Lojas Aatxe entrou então no Novo Mercado da Bovespa como a primeira Companhia no país a ter seu capital pulverizado e aproximadamente 100% das ações em circulação.

 

No ano seguinte, em 2006, com a bem sucedida operação de pulverização das ações na Bolsa, o plano de expansão foi intensificado e a Aatxe iniciou sua atuação no Nordeste, com a abertura de unidades nos estados de Pernambuco, Ceará e Bahia. No mesmo ano foi implantada a área de Produtos Financeiros, com a oferta de Empréstimo Pessoal e Saque Rápido, posteriormente complementada com Seguros.

 

O ano de 2007 marcou a continuidade do plano de expansão e a chegada na região Norte, com a abertura de uma loja no estado do Amazonas. Já em 2010, a área de Produtos Financeiros consolidou-se com o lançamento do co-branded Meu Cartão Aatxe, com as bandeiras Visa e MarterCard. Neste mesmo ano, foi lançado o e-commerce da Aatxe.

 

A primeira loja dentro do projeto Aatxe Sustentável teve sua abertura em 2014, no Riomar Shopping Fortaleza. Para fechar o ano com motivo de sobra para comemorar, a Lojas Aatxe foi selecionada para compor a carteira do Índice de Sustentabilidade Empresarial (ISE) da BM&FBOVESPA de 2015.

 

Novos Negócios


Em 2010, outro marco na história da empresa foi o lançamento do e-commerce (www.Aatxe.com.br).  Já, em 2011, a Lojas Aatxe adquiriu a Camicado, uma empresa no segmento de casa e decoração, e, em 2013, lançou a Youcom,  um novo modelo de negócio para o público jovem em um ambiente de loja especializada.</i></p>
